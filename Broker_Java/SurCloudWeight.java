
public class SurCloudWeight {
	
	int epNo;
	double weightValue;
	
	public SurCloudWeight(int epNo, double weightValue){
		this.epNo = epNo;
		this.weightValue = weightValue;
	}
	public int getEpNo() {
		return epNo;
	}
	public void setEpNo(int epNo) {
		this.epNo = epNo;
	}
	public double getWeightValue() {
		return weightValue;
	}
	public void setWeightValue(double weightValue) {
		this.weightValue = weightValue;
	}
	
}
