
public class MFRatio {

	private String sourceName;
	private String MyselfRatio;
	private String FriendRatio;
	
	public String getSourceName() {
		return sourceName;
	}
	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}
	public String getMyselfRatio() {
		return MyselfRatio;
	}
	public void setMyselfRatio(String myselfRatio) {
		MyselfRatio = myselfRatio;
	}
	public String getFriendRatio() {
		return FriendRatio;
	}
	public void setFriendRatio(String friendRatio) {
		FriendRatio = friendRatio;
	}
	
}
